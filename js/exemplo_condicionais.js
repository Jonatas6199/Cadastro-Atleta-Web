//atribuição
var valor = 1;

//comparação somente do valor
if(valor == 1)
    console.log("valor == 1");
if(valor == "1")
    console.log("valor == 1 com string");

//comparação do valor e do tipo
if(valor === 1)
    console.log("valor === 1");
if(valor === "1")
    console.log("valor === 1 com string");